package alexey;

public final class Calculator {
    int add(final int x, final int y) {
        return x + y;
    }

    int subtract(final int x, final int y) {
        return x - y;
    }

    int multiply(final int x, final int y) {
        return x * y;
    }

    int divide(final int x, final int y) {
        return x / y;
    }
}
